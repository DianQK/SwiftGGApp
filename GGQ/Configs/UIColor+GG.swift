import UIKit

extension UIColor {

    class func gg_backgroundColor() -> UIColor {
        return UIColor(red: 250.0/255.0, green: 250.0/255.0, blue: 250.0/255.0, alpha: 1)
    }

    class func gg_blackColor() -> UIColor {
        return UIColor(red: 24.0/255.0, green: 24.0/255.0, blue: 24.0/255.0, alpha: 1)
    }

    class func gg_lightBlackColor() -> UIColor {
        return UIColor(red: 105.0/255.0, green: 105.0/255.0, blue: 105.0/255.0, alpha: 1)
    }

    class func gg_whiteColor() -> UIColor {
        return UIColor.whiteColor()
    }

    class func gg_shadowsColor() -> UIColor {
        return UIColor(red: 239.0/255.0, green: 239.0/255.0, blue: 239.0/255.0, alpha: 1)
    }

}
